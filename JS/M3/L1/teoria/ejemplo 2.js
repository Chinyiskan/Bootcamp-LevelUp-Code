let lista = []; // Empezamos vacíos
console.log("Inicio:", lista);

// push → agrega AL FINAL
lista.push("Attack on Titan");
lista.push("Demon Slayer");
lista.push("One Piece");
console.log("Después de push x3:", lista);

// pop → elimina el ÚLTIMO
lista.pop();
console.log("Después de pop:", lista);

// shift → elimina el PRIMERO
lista.shift();
console.log("Después de shift:", lista);
